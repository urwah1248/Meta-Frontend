//Switch statement
var day= "sunday"
switch(day){
    case "monday": console.log("its monday"); break;
    case "tuesday": console.log("its tuesday"); break;
    case "wednesday": console.log("its wednesday"); break;
    case "thursday": console.log("its thursday"); break;
    case "friday": console.log("its friday"); break;
    case "saturday": console.log("its saturday"); break;
    case "sunday": console.log("its sunday"); break;
    default: console.log("there's no such day...")
}

