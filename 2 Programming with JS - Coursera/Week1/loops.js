//Loops

//FOR loop
for(let i=1;i<=5;i++){
    console.log(i)
}
console.log("Counting finished!")

for(let i=5;i>=1;i--){
    console.log(i)
}
console.log("Countdown finished!")

//While loop
function while1(){
    let i=1;
    while(i<=5){
        console.log(i)
        i++;
    }
    console.log("Counting Finished!")
}
while1();

function while2(){
    let i=5;
    while(i>0){
        console.log(i)
        i--;
    }
    console.log("Countdown finished!")
}
while2();

function while3(){
    let i=2018;
    while(i<=2022){
        console.log(i)
        i++
    }
}
while3();